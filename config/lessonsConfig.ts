/**
 * Get lessons by difficulty level
 */
export const getLessonsByDifficulty = (difficulty: LessonDifficulty): LessonModule[] => {
  return LESSONS_CONFIG.filter(l => l.difficulty === difficulty);
};

/**
 * Get lessons by category
 */
export const getLessonsByCategory = (category: LessonCategory): LessonModule[] => {
  return LESSONS_CONFIG.filter(l => l.category === category);
};

/**
 * Get lessons available to user based on knowledge points
 */
export const getAvailableLessons = (knowledgePoints: number): LessonModule[] => {
  return LESSONS_CONFIG.filter(l =>
    !l.requiredKnowledgePoints || knowledgePoints >= l.requiredKnowledgePoints
  );
};

/**
 * Get next recommended lesson for user
 */
export const getNextLesson = (knowledgePoints: number, completedLessonIds: string[]): LessonModule | null => {
  const available = getAvailableLessons(knowledgePoints);
  const notCompleted = available.filter(l => !completedLessonIds.includes(l.id));

  // Prioritize by difficulty (beginner first)
  const difficultyOrder: LessonDifficulty[] = ['beginner', 'intermediate', 'advanced'];
  for (const difficulty of difficultyOrder) {
    const lesson = notCompleted.find(l => l.difficulty === difficulty);
    if (lesson) return lesson;
  }

  return notCompleted[0] || null;
};
